void HAL_GPIO_EXTI_Callback(uint16_t GPIO_PIN)
{
	if (GPIO_PIN == Button_Pin)
	{

		if (HAL_GPIO_ReadPin(Button_GPIO_Port, Button_Pin) == GPIO_PIN_RESET){
			  completed = 0;
			  if (returning == 1) {
				  resetPositionReturn();
			  }
			  else {
				  resetPosition();
			  }
			  resetPID();
			  button = 1;
		}

	}

	if (GPIO_PIN == CountButton_Pin) {
		if (HAL_GPIO_ReadPin(CountButton_GPIO_Port, CountButton_Pin) == GPIO_PIN_RESET){
		  minCorrectCt++;
		  decDelay();
		}
	}
}

